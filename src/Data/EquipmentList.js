function createShortName(name) {
	return name.toLowerCase().replace(/\s/g, '');
}

//This method is only run on startup so its okay that its inefficient
function addItem(slot, name, requirements, cost, income=0, img, atk_bonus=0, mage_bonus=0, rngd_bonus=0, def_bonus=0, str_bonus=0, rngd_strength=0, mage_damage=0, pray_bonus=0) {

	let newItem = {slot, name, requirements, cost, income, img, atk_bonus, rngd_bonus, mage_bonus, def_bonus, str_bonus, rngd_strength, mage_damage, pray_bonus}

	let shortname = createShortName(name);

	if(['scimitar', 'helm', 'plate', 'kiteshield'].map( filter => { return shortname.includes(filter)}).includes(true)) {
		newItem.type = 'melee'
	} else if(['hide', 'crossbow', 'shortbow', 'arrow', 'bolt'].map(filter => { return shortname.includes(filter)}).includes(true)) {
		newItem.type = 'ranged'
	}

	if (slot === 'weapon') {
		switch (name) {
			case 'Bronze sword':
				newItem.attackstyles = ['sword-stab', 'sword-lunge-strength', 'sword-slash', 'sword-block'];
			break;
			case 'Bronze scimitar':
			case 'Iron scimitar':
			case 'Steel scimitar':
			case 'Black scimitar':
			case 'Mithril scimitar':
			case 'Adamant scimitar':
			case 'Rune scimitar':
			case 'Dragon scimitar':
				newItem.attackstyles = ['sword-chop', 'sword-slash', 'sword-lunge-shared', 'sword-block'];
			break;

			case 'Shortbow':
			case 'Oak shortbow':
			case 'Willow shortbow':
			case 'Maple shortbow':
			case 'Yew shortbow':
			case 'Magic shortbow':
				newItem.attackstyles = ['bow-accurate', 'bow-rapid', 'bow-longrange'];
				newItem.twoHanded = true;
			break;

			case 'Bronze crossbow':
			case 'Iron crossbow':
			case 'Steel crossbow':
			case 'Mithril crossbow':
			case 'Adamant crossbow':
			case 'Rune crossbow':
			case 'Dragon crossbow':
				newItem.attackstyles = ['crossbow-accurate', 'crossbow-rapid', 'crossbow-longrange'];
			break;

			case 'Staff':
				newItem.attackstyles = ['magic-bash', 'magic-defensive-spell', 'magic-focus', 'magic-pound', 'magic-spell'];
				newItem.type = 'magic'
			break;

			default:
				newItem.attackstyles = [];
			break;
		}
	}

	if(newItem.type === undefined) {
		newItem.type = null;
	}

	equipment[slot][createShortName(name)] = newItem;
}
